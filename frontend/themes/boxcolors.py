LIGHT_MODE_COLORS = [
    (44, 62, 80),
    (22, 160, 133),
    (142, 68, 173),
    (192, 57, 43),
    (211, 84, 0),
    (39, 174, 96),
    (41, 128, 185),
    (127, 140, 141),
    (243, 156, 18),
    (46, 204, 113),
]

DARK_MODE_COLORS = [
    (253, 235, 208),
    (213, 245, 227),
    (235, 222, 240),
    (252, 243, 207),
    (214, 234, 248),
    (252, 240, 226),
    (232, 218, 239),
    (249, 231, 159),
    (209, 242, 235),
    (254, 249, 231),
]

def get_box_palette(index, theme):
    if theme == 'dark':
        bg = DARK_MODE_COLORS[index % 10]
        text = (0, 0, 0)
    else:
        bg = LIGHT_MODE_COLORS[index % 10]
        text = (255, 255, 255)
    return bg, text
